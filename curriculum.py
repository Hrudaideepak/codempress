def get_subject_by_name(name: str) -> dict | None:
    """Return the subject dict for a given name."""
    for s in CURRICULUM:
        if s["name"].lower() == name.lower():
            return s
    return None


def get_topics_by_level(subject_name: str, level: str) -> list:
    """Return all topics for a subject filtered by level."""
    subject = get_subject_by_name(subject_name)
    if not subject:
        return []
    return [t for t in subject["topics"] if t["level"] == level]


def get_all_topics(subject_name: str) -> list:
    """Return all topics for a subject."""
    subject = get_subject_by_name(subject_name)
    return subject["topics"] if subject else []


def get_all_subjects() -> list:
    """Return all subject names."""
    return [s["name"] for s in CURRICULUM]


def get_subject_count() -> int:
    """Return total number of subjects."""
    return len(CURRICULUM)


def get_topic_count() -> int:
    """Return total number of topics across all subjects."""
    return sum(len(s["topics"]) for s in CURRICULUM)


def get_topic_count_by_level() -> dict:
    """Return count of topics per level."""
    counts = {"Beginner": 0, "Intermediate": 0, "Pro": 0}
    for subject in CURRICULUM:
        for topic in subject["topics"]:
            counts[topic["level"]] += 1
    return counts

